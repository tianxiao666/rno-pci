package com.hgicreate.rno.lteazimuthevaluation.model;

/**
 * Created by chen.c10 on 2016/12/16.
 * 方位角计算结果
 */
public class AzimuthResult {
    private String cellId;
    private int azimuth;
    private int azimuth1;
    private int azimuth2;
    private int diff1;
    private int diff2;

    public String getCellId() {
        return cellId;
    }

    public void setCellId(String cellId) {
        this.cellId = cellId;
    }

    public int getAzimuth() {
        return azimuth;
    }

    public void setAzimuth(int azimuth) {
        this.azimuth = azimuth;
    }

    public int getAzimuth1() {
        return azimuth1;
    }

    public void setAzimuth1(int azimuth1) {
        this.azimuth1 = azimuth1;
    }

    public int getAzimuth2() {
        return azimuth2;
    }

    public void setAzimuth2(int azimuth2) {
        this.azimuth2 = azimuth2;
    }

    public int getDiff1() {
        return diff1;
    }

    public void setDiff1(int diff1) {
        this.diff1 = diff1;
    }

    public int getDiff2() {
        return diff2;
    }

    public void setDiff2(int diff2) {
        this.diff2 = diff2;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        AzimuthResult that = (AzimuthResult) o;

        return cellId != null ? cellId.equals(that.cellId) : that.cellId == null;
    }

    @Override
    public int hashCode() {
        return cellId != null ? cellId.hashCode() : 0;
    }

    @Override
    public String toString() {
        return "AzimuthResult{" +
                "cellId='" + cellId + '\'' +
                ", azimuth=" + azimuth +
                ", azimuth1=" + azimuth1 +
                ", azimuth2=" + azimuth2 +
                ", diff1=" + diff1 +
                ", diff2=" + diff2 +
                '}';
    }
}
