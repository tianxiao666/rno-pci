package com.iscreate.rno.microservice.pci.afp.model;

public class MrJobCond {
	long jobId;
	String mrJobId;
	String account;
	String runType;

	public long getJobId() {
		return jobId;
	}

	public void setJobId(long jobId) {
		this.jobId = jobId;
	}

	public String getMrJobId() {
		return mrJobId;
	}

	public void setMrJobId(String mrJobId) {
		this.mrJobId = mrJobId;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public String getRunType() {
		return runType;
	}

	public void setRunType(String runType) {
		this.runType = runType;
	}

	public MrJobCond(long jobId, String mrJobId, String account, String runType) {
		super();
		this.jobId = jobId;
		this.mrJobId = mrJobId;
		this.account = account;
		this.runType = runType;
	}
}
