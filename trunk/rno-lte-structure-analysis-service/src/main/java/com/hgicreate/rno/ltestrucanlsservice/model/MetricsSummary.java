public class MetricsSummary {
    private String cellId;
    private String cellName;
    private String weakFlag;
    private String overlapFlag;
    private int overCnt;
    private String overFlag;
    private int over16Cnt;
    private String over16Flag;

    public String getCellId() {
        return cellId;
    }

    public void setCellId(String cellId) {
        this.cellId = cellId;
    }

    public String getCellName() {
        return cellName;
    }

    public void setCellName(String cellName) {
        this.cellName = cellName;
    }

    public String getWeakFlag() {
        return weakFlag;
    }

    public void setWeakFlag(String weakFlag) {
        this.weakFlag = weakFlag;
    }

    public String getOverlapFlag() {
        return overlapFlag;
    }

    public void setOverlapFlag(String overlapFlag) {
        this.overlapFlag = overlapFlag;
    }

    public int getOverCnt() {
        return overCnt;
    }

    public void setOverCnt(int overCnt) {
        this.overCnt = overCnt;
    }

    public String getOverFlag() {
        return overFlag;
    }

    public void setOverFlag(String overFlag) {
        this.overFlag = overFlag;
    }

    public int getOver16Cnt() {
        return over16Cnt;
    }

    public void setOver16Cnt(int over16Cnt) {
        this.over16Cnt = over16Cnt;
    }

    public String getOver16Flag() {
        return over16Flag;
    }

    public void setOver16Flag(String over16Flag) {
        this.over16Flag = over16Flag;
    }

    @Override
    public String toString() {
        return "MetricsSummary{" +
                "cellId='" + cellId + '\'' +
                ", cellName='" + cellName + '\'' +
                ", weakFlag='" + weakFlag + '\'' +
                ", overlapFlag='" + overlapFlag + '\'' +
                ", overCnt=" + overCnt +
                ", overFlag='" + overFlag + '\'' +
                ", over16Cnt=" + over16Cnt +
                ", over16Flag='" + over16Flag + '\'' +
                '}';
    }
}
