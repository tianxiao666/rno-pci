package com.hgicreate.rno.ltestrucanlsservice.model;

/**
 * Created by chen.c10 on 2016/12/19.
 * 任务查询结果
 */
public class TaskQueryResult {
    private String cityName;
    private long jobId;
    private String jobName;
    private String jobRunningStatus;
    private String begMeaTime;
    private String endMeaTime;
    private String dlFileName;
    private String launchTime;
    private String completeTime;

    public String getCityName() {
        return cityName;
    }

    public void setCityName(String cityName) {
        this.cityName = cityName;
    }

    public long getJobId() {
        return jobId;
    }

    public void setJobId(long jobId) {
        this.jobId = jobId;
    }

    public String getJobName() {
        return jobName;
    }

    public void setJobName(String jobName) {
        this.jobName = jobName;
    }

    public String getJobRunningStatus() {
        return jobRunningStatus;
    }

    public void setJobRunningStatus(String jobRunningStatus) {
        this.jobRunningStatus = jobRunningStatus;
    }

    public String getBegMeaTime() {
        return begMeaTime;
    }

    public void setBegMeaTime(String begMeaTime) {
        this.begMeaTime = begMeaTime;
    }

    public String getEndMeaTime() {
        return endMeaTime;
    }

    public void setEndMeaTime(String endMeaTime) {
        this.endMeaTime = endMeaTime;
    }

    public String getDlFileName() {
        return dlFileName;
    }

    public void setDlFileName(String dlFileName) {
        this.dlFileName = dlFileName;
    }

    public String getLaunchTime() {
        return launchTime;
    }

    public void setLaunchTime(String launchTime) {
        this.launchTime = launchTime;
    }

    public String getCompleteTime() {
        return completeTime;
    }

    public void setCompleteTime(String completeTime) {
        this.completeTime = completeTime;
    }

    @Override
    public String toString() {
        return "TaskQueryResult{" +
                "cityName='" + cityName + '\'' +
                ", jobId=" + jobId +
                ", jobName='" + jobName + '\'' +
                ", jobRunningStatus='" + jobRunningStatus + '\'' +
                ", begMeaTime='" + begMeaTime + '\'' +
                ", endMeaTime='" + endMeaTime + '\'' +
                ", dlFileName='" + dlFileName + '\'' +
                ", launchTime='" + launchTime + '\'' +
                ", completeTime='" + completeTime + '\'' +
                '}';
    }
}
