package com.hgicreate.rno.lte.pciafp.model;

public enum JobParseStatus {

    Waiting("Waiting"), Running("Running"), Fail("Fail"), Succeeded("Succeeded");

    String desc;

    JobParseStatus(String desc) {
        this.desc = desc;
    }

    @Override
    public String toString() {
        return this.desc;
    }
}
